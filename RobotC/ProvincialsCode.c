void auton1()	{ // First Autonomous program

	while (true)	{

		liftPos = SensorValue[lift];
		clawPos = SensorValue[grabber];
		leftPos = -SensorValue[leftDrive];
		rightPos = -SensorValue[rightDrive];

		switch (state)	{

		case 0:

			SensorValue[leftDrive] = 0;
			SensorValue[rightDrive] = 0;

			resetVar();

			state++;

			break;

		case 1:

			grab(clawHalf);

			if(grabDone)	{

				resetVar();

				state++;

			}

			break;

		case 2:

			drive(760, 760);

			if(driveDone)	{

				resetVar();

				state++;

			}

			break;

		case 3:

			grab(2250);

			if(grabDone)	{

				resetVar();

				state++;

			}

			break;

		case 4:

			drive(1450, 1450);

			launch(liftMid);

			if(driveDone)	{

				resetVar();

				state++;

			}

			break;

		case 5:

			drive(800, 1950);

			if(driveDone)	{

				resetVar();

				state++;

			}

			break;

		case 6:

			drive(0, 1150);

			checkLift();

			if (leftPos <= 600)	{

				launch(liftTop);

			}

			if(driveDone)	{

				resetVar();

				state++;

			}

			break;

		case 7:

			launch(liftDown);

			if(liftDone)	{

				resetVar();

				state++;

			}

			break;

		case 8:

			drive(1150, 2300);

			if(driveDone)	{

				resetVar();

				state++;

			}

			break;

		case 9:

			drive(650, 1650);

			grab(clawClose);

			if(driveDone && grabDone)	{

				resetVar();

				state++;

			}

			break;

		case 10:

			drive(20, 1020);

			launch(liftTop);

			if(driveDone)	{

				resetVar();

				state++;

			}

			break;

		case 11:

			launch(liftDown);

			if(liftDone)	{

				resetVar();

				state++;

			}

			break;

		case 12:

			drive(1150, 2300);

			if(driveDone)	{

				resetVar();

				state++;

			}

			break;

		case 13:

			drive(650, 1650);

			grab(clawClose);

			if(driveDone && grabDone)	{

				resetVar();

				state++;

			}

			break;

		case 14:

			drive(20, 1020);

			launch(liftTop);

			if(driveDone)	{

				resetVar();

				state++;

			}

			break;

		default:

			motor[driveL] = 0;
			motor[driveR] = 0;

			motor[liftL1] = 0;
			motor[liftL2] = 0;
			motor[liftR1] = 0;
			motor[liftR2] = 0;

			motor[grabL] = 0;
			motor[grabR] = 0;

			break;


		}

	}

}

task autonomous()
{

	SensorValue[leftDrive] = 0;
	SensorValue[rightDrive] = 0;

	state = 0;

	auton1();
}

