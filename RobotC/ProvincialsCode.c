task usercontrol()
{

	SensorValue[leftDrive] = 0;
	SensorValue[rightDrive] = 0;

	while (true)
	{

		liftPos = SensorValue[lift];
		clawPos = SensorValue[grabber];
		leftPos = -SensorValue[leftDrive];
		rightPos = -SensorValue[rightDrive];

		if(abs(vexRT[Ch3]) > threshold)	{ // Turns left drive the same amount as specified by the joystick if the joystick value is over 15

			motor[driveL] = vexRT[Ch3];

			}	else	{

			motor[driveL] = 0;

		}

		if(abs(vexRT[Ch2]) > threshold)	{ // Turns right drive the same amount as specified by the joystick if the joystick value is over 15

			motor[driveR] = vexRT[Ch2];

			}	else	{

			motor[driveR] = 0;

		}

		if(vexRT[Btn5U]==1)	{

			motor[liftL1] = 127;
			motor[liftL2] = 127;
			motor[liftR1] = 127;
			motor[liftR2] = 127;

			}	else	if (vexRT[Btn5D] ==1)	{

			motor[liftL1] = -127;
			motor[liftL2] = -127;
			motor[liftR1] = -127;
			motor[liftR2] = -127;

			}	else	{

			motor[liftL1] = 0;
			motor[liftL2] = 0;
			motor[liftR1] = 0;
			motor[liftR2] = 0;

		}

		if(vexRT[Btn6D]==1)	{

			motor[grabL] = 127;
			motor[grabR] = 127;

			}	else	if (vexRT[Btn6U] ==1)	{

			motor[grabL] = -127;
			motor[grabR] = -127;

			}	else	{

			motor[grabL] = 0;
			motor[grabR] = 0;
		}

		if(vexRT[Btn7D] == 1)	{

			if(manualOpen)	{

				manualOpen = false;

				}	else	{

				manualOpen = true;

			}

		}

		checkLift();

	}
}
