// Autonomous Functions - Fucntions used in the autonomous task to control robot

void drive(int left, int right)	{

	int currentL = -SensorValue[leftDrive];
	int currentR = -SensorValue[rightDrive];

	int leeway = 2;

	if(currentL > (left + leeway))	{

		motor[driveL] = -127;

		}	else	if(currentL < (left - leeway))	{

		motor[driveL] = 127;

		}	else	{

		motor[driveL] = 0;

		leftDone = true;
	}

	if(currentR > (right + leeway))	{

		motor[driveR] = -127;

		}	else	if(currentR < (right - leeway))	{

		motor[driveR] = 127;

		}	else	{

		motor[driveR] = 0;

		rightDone =  true;

	}

	if(rightDone && leftDone)	{

		driveDone =  true;

		}	else	{

		driveDone = false;

	}

}

void launch(int degree)	{

	int current = SensorValue[lift];

	int leeway = 10;

	if(current > (degree + leeway))	{

		motor[liftL1] = 127;
		motor[liftL2] = 127;
		motor[liftR1] = 127;
		motor[liftR2] = 127;

		}	else	if (current < (degree - leeway))	{

		motor[liftL1] = -127;
		motor[liftL2] = -127;
		motor[liftR1] = -127;
		motor[liftR2] = -127;

		}	else	{

		motor[liftL1] = 0;
		motor[liftL2] = 0;
		motor[liftR1] = 0;
		motor[liftR2] = 0;

		liftDone = true;

	}

}

void grab(int degree)	{

	int current = SensorValue[grabber];

	int leeway = 10;

	if(current > (degree + leeway))	{

		motor[grabL] = -127;
		motor[grabR] = -127;

		}	else	if (current < (degree - leeway))	{

		motor[grabL] = 127;
		motor[grabR] = 127;

		}	else	{

		motor[grabL] = 0;
		motor[grabR] = 0;

		grabDone = true;

	}

}

void checkLift()	{ // Checks if claw should start opening

	if(liftPos <= liftTop)	{

		openClaw = true;

	}

	if(openClaw && !manualOpen)	{

		if(clawPos <= clawHalf)	{

			motor[grabL] = 0;
			motor[grabR] = 0;

			openClaw = false;

			}	else	{

			motor[grabL] = -127;
			motor[grabR] = -127;


		}

	}

}

void resetVar()	{ // Resets autonomous booleans

	leftDone =  false;
	rightDone =  false;
	liftDone =  false;
	grabDone =  false;
	driveDone =  false;

}

