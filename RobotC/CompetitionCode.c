void claw(int degree)	{ // Method to adjust claw position with the direction and degree

	clawVal = SensorValue[s1];

	if(clawVal >= degree + 50)	{ // If claw wants to open

		motor[g1] = -127;

		clawDone = false;

		}	else if (clawVal <= degree - 50)	{ // If claw wants to close

		motor[g1] = 127;

		clawDone = false;

		}	else	{

		motor[g1] = 0;

		clawDone = true;

	}

}

void lift(int delta)	{ // Method to adjust lift arm position by direction of change in rotation

	int currentLiftVal = SensorValue[s2];

	if (currentLiftVal > (liftVal + delta + 5))	{

		motor[l1] = -127;
		motor[l2] = -127;
		motor[l3] = -127;
		motor[l4] = -127;
		motor[l6] = -127;


		liftDone = false;

		}	else if (currentLiftVal < (liftVal + delta - 5))	{

		motor[l1] = 127;
		motor[l2] = 127;
		motor[l3] = 127;
		motor[l4] = 127;
		motor[l6] = 127;

		liftDone = false;

		}	else	{

		motor[l1] = 0;
		motor[l2] = 0;
		motor[l3] = 0;
		motor[l4] = 0;
		motor[l6] = 0;

		liftDone = true;

	}

}

void drive(int left, int right)	{ // Method which automatically drives the robot

	if (SensorValue[selector] == 1)	{

		motor[d2] = left;
		motor[d1] = right;

		}	else	{

		motor[d1] = left;
		motor[d2] = right;

	}

}

task autonomous()
{

	int t = 0;

	while(true)	{

		switch (state)	{

		case 0 :

			liftVal = SensorValue[s2];

			state++;

			break;

		case 1 :

			claw(clawHalf);

			if (!liftDone)	{

				lift(liftBig);

			}

			if (t <= 1000)	{

				drive(127, 127);

				}	else	{

				drive(0, 0);

			}

			if (clawDone && liftDone)	{

				state++;

				t = 0;

			}

			break;

		case 2 :

			state++;

			break;

		case 3 :

			drive (0, 127);

			if (t > 650)	{

				state++;

				t = 0;

			}

			break;

		case 4 :

			drive (127, 127);

			if (t > 700)	{

				state++;

				t = 0;

			}

			break;

		case 5 :

			drive (-127, -127);

			if (t > 700)	{

				state++;

				t = 0;

			}

			break;


		case 6 :

			if (SensorValue[ifDisco] == 1)	{

				drive (0, -127);


				if (t > 650)	{

					state++;

					t = 0;

				}

				}	else	{

				drive(0,0);

			}

			break;


		case 7 :

			lift(1);

			drive (-127, -127);

			if (t > 500)	{

				state++;

				t = 0;

			}

			break;

		case 8 :

			claw(500);

			drive(60,-127);

			if (t > 450)	{

				state++;

				t = 0;

			}

			break;

		case 9 :

			drive (-127, -127);

			if (t > 600)	{

				state++;

				t = 0;

			}

			break;

		case 10 :

			drive (127, 127);
			claw(1300);

			if (t > 2000)	{

				state++;

				t = 0;

			}

			break;

		case 11 :

			drive (0, 0);
			claw(500);

			if (t > 500)	{

				state++;

				t = 0;

			}

			break;

		case 12 :

			drive(-127, -127);

			if (!liftDone)	{

				lift(liftBig);

			}

			if (t > 500)	{

				state++;

				t = 0;

			}

			break;

		case 13 :

			drive (-127, 127);

			if (t > 700)	{

				state++;

				t = 0;

			}

			break;

		case 14 :

			drive (127, 127);

			if (t > 1500)	{

				state++;

				t = 0;

			}

			break;

		case 15 :

			drive(0, 0);

			claw(clawHalf);

			if(clawDone)	{

				state++;

				clawDone = false;

			}

			break;

		default :

			break;

		}

		sleep(1);
		t++;

	}

}

