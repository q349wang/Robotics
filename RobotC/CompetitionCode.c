task usercontrol()
{

	//int driveSpeed = 0;
	//int turnSpeed = 0;
	int driveL = 0, driveR = 0;

	const int threshold = 15;

	while (true)	{

		clawVal = SensorValue[s1];
		liftVal = SensorValue[s2];

		// Two Joystick Drive v2 - BAYAN EDITION

		driveL = vexRT[Ch3];
		driveR = vexRT[Ch2];

		if(abs(driveR) >= threshold)	{

			motor[d1] = driveR;

			}	else {

			motor[d1] = 0;

		}
		if(abs(driveL) >= threshold)	{

			motor[d2] = driveL;

			}else	{


			motor[d2] = 0;

		}

		if(vexRT[Btn6U] == 1)	{

			motor[g1] = 127;

			}	else if(vexRT[Btn6D] == 1)	{

			motor[g1] = -127;

			}	else	{

			motor[g1] = 0;

		}

		if (vexRT[Btn5U] == 1)	{

			motor[l1] = 127;
			motor[l2] = 127;
			motor[l3] = 127;
			motor[l4] = 127;
			motor[l6] = 127;

			}	else if (vexRT[Btn5D] == 1)	{

			motor[l1] = -127;
			motor[l2] = -127;
			motor[l3] = -127;
			motor[l4] = -127;
			motor[l6] = -127;

			}	else	{

			motor[l1] = 0;
			motor[l2] = 0;
			motor[l3] = 0;
			motor[l4] = 0;
			motor[l6] = 0;

		}

	}
}
