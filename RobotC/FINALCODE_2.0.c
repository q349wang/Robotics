task autonomous()
{

	SensorValue[leftDrive] = 0;
	SensorValue[rightDrive] = 0;

	state = 0;

	if(SensorValue[cubeAuton] == 1)	{

		auton1(); // Cube auton

		}	else	{

		auton2(); // Drive forward auton

	}
}

