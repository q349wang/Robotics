// Autonomous Functions - Fucntions used in the autonomous task to control robot

void drive(int left, int right, int speed)	{

	if (!driveDone)	{

		if(SensorValue[switchDirection] == 0)	{ // Switches direction of drive if desired by jumper in digital 12 port

			int temp;

			temp = left;
			left = right;
			right = temp;

		}


		int currentL = -SensorValue[leftDrive];
		int currentR = -SensorValue[rightDrive];

		int leeway = 2;

		if(currentL > (left + leeway))	{

			motor[driveL] = -speed;

			}	else	if(currentL < (left - leeway))	{

			motor[driveL] = speed;

			}	else	{

			motor[driveL] = 0;

			leftDone = true;
		}

		if(currentR > (right + leeway))	{

			motor[driveR] = -speed;

			}	else	if(currentR < (right - leeway))	{

			motor[driveR] = speed;

			}	else	{

			motor[driveR] = 0;

			rightDone =  true;

		}

		if(rightDone && leftDone)	{

			driveDone =  true;

			}	else	{

			driveDone = false;

		}

	}

}

void launch(int degree, int speed)	{

	int current = SensorValue[lift];

	int leeway = 20;

	if(current > (degree + leeway))	{

		motor[liftL1] = speed;
		motor[liftL2] = speed;
		motor[liftR1] = speed;
		motor[liftR2] = speed;

		}	else	if (current < (degree - leeway))	{

		motor[liftL1] = -speed;
		motor[liftL2] = -speed;
		motor[liftR1] = -speed;
		motor[liftR2] = -speed;

		}	else	{

		motor[liftL1] = 0;
		motor[liftL2] = 0;
		motor[liftR1] = 0;
		motor[liftR2] = 0;

		liftDone = true;

	}


}

void grab(int degree)	{

	int current = SensorValue[grabber];

	int leeway = 10;

	if(current > (degree + leeway))	{

		motor[grabL] = -127;
		motor[grabR] = -127;

		}	else	if (current < (degree - leeway))	{

		motor[grabL] = 127;
		motor[grabR] = 127;

		}	else	{

		motor[grabL] = 0;
		motor[grabR] = 0;

		grabDone = true;

	}

}

void checkLift()	{ // Checks if claw should start opening

	if((liftPos <= liftTop) && (liftPos > 500))	{

		openClaw = true;

		}	else	{

		openClaw = false;

	}

	if(openClaw && !manualOpen)	{

		if(clawPos <= 2300)	{

			motor[grabL] = 0;
			motor[grabR] = 0;

			openClaw = false;

			}	else	{

			motor[grabL] = -127;
			motor[grabR] = -127;


		}

		}	else	{

		openClaw = false;

	}

}

void holdLift()	{

	if(liftPos >= liftMid)	{

		motor[liftL1] = 90;
		motor[liftL2] = 90;
		motor[liftR1] = 90;
		motor[liftR2] = 90;

		}	else	{

		motor[liftL1] = 0;
		motor[liftL2] = 0;
		motor[liftR1] = 0;
		motor[liftR2] = 0;

	}

}

void resetVar()	{ // Resets autonomous booleans

	leftDone =  false;
	rightDone =  false;
	liftDone =  false;
	grabDone =  false;
	driveDone =  false;

}

