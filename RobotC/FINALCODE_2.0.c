void auton1()	{ // First Autonomous program

	while (true)	{

		liftPos = SensorValue[lift];
		clawPos = SensorValue[grabber];
		leftPos = -SensorValue[leftDrive];
		rightPos = -SensorValue[rightDrive];

		switch (state)	{

		case 0:

			SensorValue[leftDrive] = 0;
			SensorValue[rightDrive] = 0;

			resetVar();

			state++;

			break;
		case 1:
			drive(10, 10, 127);
			if(driveDone)
			{
				resetVar();
				state ++;
			}
			break;

		case 2:

			grab(2400);

			if(grabDone)	{

				resetVar();

				state++;

			}

			break;

		case 3:

			drive(860, 860, 127);

			grab(clawHalf);

			if(driveDone)	{

				resetVar();

				state++;

				clearTimer(T1);

			}

			break;

		case 4:
			drive(860, 960, 127);
			grab(2550);
			if(grabDone || (time1[T1] > 500))
			{
				resetVar();

				state++;
			}



			break;

		case 5:

			motor[grabL] = 127;
			motor[grabR] = 127;

			state++;

			break;
		case 6: //turns left
			drive(420,1400, 127);
			launch(liftTop,80);
			if(driveDone)
			{
				resetVar();
				state++;
			}
			break;
		case 7:
			drive(-200, 800, 90);
			if(driveDone)	{

				resetVar();

				state++;

			}
			break;

		case 8:

			grab(clawHalf + 300);

			if (grabDone)	{

				resetVar();

				state++;
			}

			break;

		default:

			motor[driveL] = 0;
			motor[driveR] = 0;

			motor[liftL1] = 0;
			motor[liftL2] = 0;
			motor[liftR1] = 0;
			motor[liftR2] = 0;

			motor[grabL] = 0;
			motor[grabR] = 0;

			break;


		}

	}

}

